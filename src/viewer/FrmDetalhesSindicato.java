    private void preencheDados(){
        txtCodigo.setText(String.valueOf(sind.getCodigo()));
        txtSigla.setText(sind.getSigla());
        txtDesignacao.setText(sind.getDesignacao());
        txtRepresentante.setText(sind.getRepresentante());
        Distrito dist = new Distrito().findDistrito(sind.getCodDist());
        Provincia prov = new Provincia().findProvincia(dist.getCodProv());
        txtProvincia.setText(prov.getNome());
        txtDistrito.setText(dist.getNome());
        txtEndereco.setText(sind.getEndereco());
        txtContacto.setText(sind.getContacto());
        txtData.setText(String.valueOf(sind.getData()));
        Categoria categ = new Categoria().findCategoria(sind.getCategoria());
        txtCategoria.setText(categ.getDesignacao());
        EstadoSindicato estado = new EstadoSindicato().findEstado(sind.getEstado());
        txtEstado.setText(estado.getEstado());
        TipoRegisto tipo = new TipoRegisto().findEstado(categ.getCodTipo());
        txtTipo.setText(tipo.getDesignacao());
        if(sind.getCodActiv() != 0){
            Actividade activ = new Actividade().findActividade(sind.getCodActiv());
            txtSector.setText(activ.getNome());
        }        
        txtDataDespacho.setText(String.valueOf(sind.getDataDespacho()));
        txtDespacho.setText(sind.getDespacho());
        if(sind.getNumInss() != 0){
            txtInss.setText(String.valueOf(sind.getNumInss()));
            btOpenEmpresa.setEnabled(true);
        }
        if(sind.getFileActa().length()>5){
            txtFileActa.setText(sind.getFileActa());
            btOpenActa.setEnabled(true);
        }
        if(sind.getFileEstatuto().length()>5){
            txtFileEstatuto.setText(sind.getFileEstatuto());
            btOpenEstatuto.setEnabled(true);
        }
        if(sind.getFileLista().length()>5){
            txtFilePresencas.setText(sind.getFileLista());
            btOpenPresencas.setEnabled(true);
        }
        if(sind.getFileCertidao().length()>5){
            txtFileCertidao.setText(sind.getFileCertidao());
            btOpenCertidao.setEnabled(true);
        }
        if(sind.getFileConvocatoria().length()>5){
            txtFileConvocatoria.setText(sind.getFileConvocatoria());
            btOpenConvocatoria.setEnabled(true);
        }
    }
    
