    public int search(Sindicato sinds[], int nrElem, int codigo){
        int index =0;
        for(int i =0; i<nrElem; i++){
            if(sinds[i].getCodigo() == codigo){
                index = i;
                break;
            }
        }
        return index;
    }
    
    // Grava todos as empresas do array no ficheiro de texto
    private void updateTextFile(Sindicato[] sinds, int nrElem){
    	try{
            FileWriter fileText = new FileWriter(new File("C:\\MyFiles\\GestaoSindicato\\SINDICATO.txt"));
            BufferedWriter writeText = new BufferedWriter(fileText);
            for(int i=0; i< nrElem ; i++){
                writeText.write(sinds[i].toString());
                writeText.newLine();
            }
            writeText.close();
            JOptionPane.showMessageDialog(null, "Sindicato inserido com sucesso","Inserção", JOptionPane.INFORMATION_MESSAGE);    		
    	}catch(Exception ex){
            JOptionPane.showMessageDialog(null, "ERRO NOMANUSEAMENTO DO FICHEIRO:\n"+ex.getMessage(),"Escrita do Ficheiro", JOptionPane.ERROR_MESSAGE);
            ex.printStackTrace();
    	}
    }
    
    
