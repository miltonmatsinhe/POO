    private void btAprovarActionPerformed(java.awt.event.ActionEvent evt) {//GEN-FIRST:event_btAprovarActionPerformed
        if(!txtCodigo.getText().isEmpty()){
            int codigo = Integer.parseInt(txtCodigo.getText());
            String despacho ;             
            if(rbAprovar.isSelected()){
                despacho = "Processo devidamente instruido, Registo Aprovado"; 
                aprovaRegisto(codigo, despacho);
            }else{
                String motivos ="";
                if(ckConvocatoria.isSelected()){
                    motivos += ckConvocatoria.getText() +"; ";
                }
                if(ckLista.isSelected()){
                    motivos += ckLista.getText() +"; ";
                }
                if(ckActa.isSelected()){
                    motivos += ckActa.getText() +"; ";
                }
                if(ckActa.isSelected()){
                    motivos += ckActa.getText() +"; ";
                }
                if(ckCertidao.isSelected()){
                    motivos += ckCertidao.getText() +"; ";
                }
                despacho = "Registo Rejeitado deve regularizar os documentos : "; 
                rejeitarRegisto(codigo, despacho + motivos);
            }
        }
    }//GEN-LAST:event_btAprovarActionPerformed

