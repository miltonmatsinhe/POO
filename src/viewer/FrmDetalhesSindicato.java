    private void aprovaRegisto(int codigo, String despacho){
        try{           
             String data = new SimpleDateFormat("yyyy-MM-dd").format(new java.util.Date());
             System.out.println(data);
             Object regs[][] = new Sindicato().loadSindicatos();
             Sindicato sindicatos[] = (Sindicato[])regs[0][0];
             int nrElem = (int)regs[0][1];
             int index = search(sindicatos, nrElem, codigo);
             if(index != -1){
                 sindicatos[index].setEstado(2);
                 sindicatos[index].setDataDespacho(Date.valueOf(data));
                 sindicatos[index].setDespacho(despacho);
             }
             updateTextFile(sindicatos, nrElem);
            JOptionPane.showInternalMessageDialog(null, "Sindicato Aprovado com sucesso", "despacho do registo", JOptionPane.INFORMATION_MESSAGE);
             txtEstado.setText("APROVADO");
             txtDataDespacho.setText(data);
             txtDespacho.setText(despacho);
             btAprovar.setEnabled(false);               
        }catch(Exception ex){
            JOptionPane.showInternalMessageDialog(null, "Erro na Aprovação do Registo do Sindicato \n "+ex.getMessage(), "select", JOptionPane.ERROR_MESSAGE);
            ex.printStackTrace();
        }
    }
    
